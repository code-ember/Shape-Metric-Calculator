public class Rectangle {

    // Declare double variables width and height
    double length;
    double width;

    // No-argument constructor with default values
    public Rectangle() {
        this.length = 1.0;
        this.width = 1.0;
    }

    // Constructor for Rectangle class that takes width and height
    public Rectangle(double length, double width) {
        this.length = length;
        this.width = width;
    }
}
